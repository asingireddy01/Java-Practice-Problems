public class EscapeCharacters
{
    public static void main(String[] args)
    {
        System.out.println("I solemnly");
        System.out.println("swear");
        System.out.println("that");
        System.out.print("I'm up to no good.");

        System.out.println("");
        System.out.println("");
        System.out.println("This is a\ttest"); // tab
        System.out.println("This\nis\na\ntest"); // new line
        System.out.println("I like to put \"quotes\""); // quote
        System.out.println("This will add a \\backslash"); // backslash

        System.out.println("");
        System.out.println("");
        System.out.println("Abhiram\tSingireddy");
        System.out.println("Blue");
        System.out.println("7/9/2021");
        System.out.println("\"Too many have dispensed with generosity in order to practice charity\"");
    }
}
